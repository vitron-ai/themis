function readGenerateMap(mapFile, projectRoot) {
  if (!fs.existsSync(mapFile)) {
    return { entries: [] };
  }

  try {
    const payload = JSON.parse(fs.readFileSync(mapFile, 'utf8'));
    const entries = Array.isArray(payload.entries) ? payload.entries.map((entry) => ({
      sourceKey: safeRealpath(path.resolve(projectRoot, entry.sourceFile)),
      sourceFile: entry.sourceFile,
      sourceHash: entry.sourceHash,
      testFile: entry.testFile,
      moduleKind: entry.moduleKind,
      confidence: entry.confidence,
      exactExports: entry.exactExports,
      exportNames: entry.exportNames,
      hintsFile: entry.hintsFile ? path.resolve(projectRoot, entry.hintsFile) : null,
      scenarios: entry.scenarios || []
    })) : [];
    return { entries };
  } catch (error) {
    throw new Error(`Failed to parse generate map artifact at ${mapFile}: ${error.message}`);
  }
}

