function normalizeGenerateOptions(projectRoot, options) {
  const outputDir = options.outputDir ? path.resolve(projectRoot, options.outputDir) : undefined;
  const requestedConfidence = normalizeConfidenceOption(options.requireConfidence);
  const strict = Boolean(options.strict);

  return {
    targetDir: options.targetDir || 'src',
    outputDir,
    force: Boolean(options.force),
    strict,
    writeHints: Boolean(options.writeHints),
    review: Boolean(options.review || options.plan),
    update: Boolean(options.update),
    clean: Boolean(options.clean),
    changed: Boolean(options.changed),
    plan: Boolean(options.plan),
    failOnSkips: Boolean(strict || options.failOnSkips),
    failOnConflicts: Boolean(!Boolean(options.review || options.plan) || strict || options.failOnConflicts),
    requireConfidence: strict
      ? pickHigherConfidence(requestedConfidence, 'high')
      : requestedConfidence,
    scenario: normalizeScenarioOption(options.scenario),
    minConfidence: normalizeConfidenceOption(options.minConfidence),
    matchSource: compileOptionalRegex(options.matchSource, '--match-source'),
    matchExport: compileOptionalRegex(options.matchExport, '--match-export'),
    include: compileOptionalRegex(options.include, '--include'),
    exclude: compileOptionalRegex(options.exclude, '--exclude'),
    files: normalizeFileSelection(projectRoot, options.files)
  };
}

function compileOptionalRegex(value, label) {
  if (!value) {
    return null;
  }

  try {
    return new RegExp(value);
  } catch (error) {
    throw new Error(`Invalid ${label} regex: ${value}`);
  }
}

function normalizeFileSelection(projectRoot, files) {
  if (!files) {
    return null;
  }

  const values = Array.isArray(files)
    ? files
    : String(files)
      .split(',')
      .map((entry) => entry.trim())
      .filter(Boolean);

  if (values.length === 0) {
    return null;
  }

  return values.map((entry) => path.resolve(projectRoot, entry));
}

function normalizeScenarioOption(value) {
  if (!value) {
    return null;
  }

  if (!SCENARIO_PRIORITY.includes(value)) {
    throw new Error(
      `Invalid --scenario value: ${value}. Use one of: ${SCENARIO_PRIORITY.join(', ')}.`
    );
  }

  return value;
}

function normalizeConfidenceOption(value) {
  if (!value) {
    return null;
  }

  if (!CONFIDENCE_PRIORITY.includes(value)) {
    throw new Error(
      `Invalid --min-confidence value: ${value}. Use one of: ${CONFIDENCE_PRIORITY.join(', ')}.`
    );
  }

  return value;
}

function isConfidenceAtLeast(confidence, minimum) {
  return CONFIDENCE_PRIORITY.indexOf(confidence) >= CONFIDENCE_PRIORITY.indexOf(minimum);
}

function pickHigherConfidence(left, right) {
  if (!left) {
    return right || null;
  }
  if (!right) {
    return left;
  }
  return isConfidenceAtLeast(left, right) ? left : right;
}

function resolveScanTarget(projectRoot, targetDir) {
  const requestedPath = path.resolve(projectRoot, targetDir);

  if (!fs.existsSync(requestedPath)) {
    throw new Error(`Generate target not found: ${formatPathForDisplay(projectRoot, requestedPath)}`);
  }

  const stat = fs.statSync(requestedPath);
  if (stat.isDirectory()) {
    return {
      requestedPath,
      isFile: false,
      mirrorBase: requestedPath
    };
  }

  if (!stat.isFile() || !isEligibleSourceFile(requestedPath)) {
    throw new Error(`Generate target is not a supported source file: ${formatPathForDisplay(projectRoot, requestedPath)}`);
  }

  return {
    requestedPath,
    isFile: true,
    mirrorBase: inferMirrorBase(projectRoot, requestedPath)
  };
}

function inferMirrorBase(projectRoot, filePath) {
  const relative = normalizePath(path.relative(projectRoot, filePath));
  const segments = relative.split('/');

  if (segments[0] === 'src') {
    return path.join(projectRoot, 'src');
  }

  return path.dirname(filePath);
}

function selectSourceFiles({ scanTarget, outputDir, projectRoot, options, changedSet }) {
  const files = [];

  if (scanTarget.isFile) {
    files.push(scanTarget.requestedPath);
  } else {
    discoverSourceFiles(scanTarget.requestedPath, outputDir, files);
  }

  const selected = files.filter((file) => {
    if (options.files && !options.files.some((selectedFile) => safeRealpath(selectedFile) === safeRealpath(file))) {
      return false;
    }

    const relativeFile = formatPathForDisplay(projectRoot, file);

    if (options.include && !options.include.test(relativeFile)) {
      return false;
    }
    if (options.exclude && options.exclude.test(relativeFile)) {
      return false;
    }
    if (options.matchSource && !options.matchSource.test(relativeFile)) {
      return false;
    }
    if (changedSet && !changedSet.has(safeRealpath(file))) {
      return false;
    }

    return true;
  });

  return selected.sort();
}

function collectChangedPaths(projectRoot) {
  let output;

  try {
    output = execFileSync(
      'git',
      ['status', '--short', '--untracked-files=all'],
      { cwd: projectRoot, encoding: 'utf8' }
    );
  } catch (error) {
    throw new Error('--changed requires a git worktree.');
  }

  const changed = new Set();
  for (const rawLine of String(output).split('\n')) {
    if (!rawLine.trim()) {
      continue;
    }

    const status = rawLine.slice(0, 2);
    const remainder = rawLine.slice(3).trim();
    const target = remainder.includes(' -> ') ? remainder.split(' -> ').pop() : remainder;
    const resolved = path.resolve(projectRoot, target);
    changed.add(safeRealpath(resolved));

    if (status.includes('R')) {
      const previous = remainder.split(' -> ')[0];
      changed.add(safeRealpath(path.resolve(projectRoot, previous)));
    }
  }

  return changed;
}

function analyzeSourceFile(filePath) {
  const source = fs.readFileSync(filePath, 'utf8');
  const sourceFile = ts.createSourceFile(
    filePath,
    source,
    ts.ScriptTarget.Latest,
    true,
    resolveScriptKind(filePath)
  );
  const typeIndex = collectTypeIndex(sourceFile);
  const state = {
    file: filePath,
    source,
    exportNames: new Set(),
    exportMap: new Map(),
    exactExports: true,
    hasRuntimeExports: false,
    skipReason: null,
    importsReact: false,
    usesJsx: path.extname(filePath).toLowerCase() === '.jsx' || path.extname(filePath).toLowerCase() === '.tsx'
  };

  for (const statement of sourceFile.statements) {
    collectImports(statement, state);
    collectExports(statement, state, typeIndex);
  }

  if (!state.hasRuntimeExports) {
    return {
      file: filePath,
      source,
      exportNames: [],
      exportEntries: [],
      exactExports: false,
      hasRuntimeExports: false,
      skipReason: 'No runtime exports discovered.',
      importsReact: state.importsReact,
      usesJsx: state.usesJsx
    };
  }

  const exportEntries = [...state.exportMap.values()].sort((a, b) => a.exportedName.localeCompare(b.exportedName));

  return {
    file: filePath,
    source,
    exportNames: [...state.exportNames].sort(),
    exportEntries,
    exactExports: state.exactExports,
    hasRuntimeExports: true,
    skipReason: null,
    importsReact: state.importsReact,
    usesJsx: state.usesJsx
  };
}

function collectImports(statement, state) {
  if (ts.isImportDeclaration(statement)) {
    const moduleName = readModuleSpecifier(statement.moduleSpecifier);
    if (moduleName === 'react' || moduleName === 'react/jsx-runtime') {
      state.importsReact = true;
    }
    return;
  }

  if (!ts.isVariableStatement(statement)) {
    return;
  }

  for (const declaration of statement.declarationList.declarations) {
    if (
      declaration.initializer &&
      ts.isCallExpression(declaration.initializer) &&
      ts.isIdentifier(declaration.initializer.expression) &&
      declaration.initializer.expression.text === 'require'
    ) {
      const moduleName = declaration.initializer.arguments[0] ? readKnownPropertyName(declaration.initializer.arguments[0]) : null;
      if (moduleName === 'react' || moduleName === 'react/jsx-runtime') {
        state.importsReact = true;
      }
    }
  }
}

function collectTypeIndex(sourceFile) {
  const index = new Map();

  for (const statement of sourceFile.statements) {
    if (ts.isInterfaceDeclaration(statement)) {
      const fields = readTypeMembers(statement.members, index);
      if (fields) {
        index.set(statement.name.text, fields);
      }
      continue;
    }

    if (ts.isTypeAliasDeclaration(statement) && ts.isTypeLiteralNode(statement.type)) {
      const fields = readTypeMembers(statement.type.members, index);
      if (fields) {
        index.set(statement.name.text, fields);
      }
    }
  }

  return index;
}

function readTypeMembers(members, typeIndex) {
  const fields = [];

  for (const member of members) {
    if (!ts.isPropertySignature(member) || !member.name) {
      continue;
    }

    const propertyName = getPropertyName(member.name);
    if (!propertyName) {
      continue;
    }

    const descriptor = resolveTypeDescriptor(member.type, typeIndex);
    fields.push({
      name: propertyName,
      optional: Boolean(member.questionToken),
      sampleValue: descriptor.sampleValue,
      inferredType: descriptor.inferredType
    });
  }

  return fields;
}

function resolveTypeDescriptor(typeNode, typeIndex) {
  if (!typeNode) {
    return { inferredType: 'unknown', sampleValue: undefined };
  }

  if (typeNode.kind === ts.SyntaxKind.StringKeyword) {
    return { inferredType: 'string', sampleValue: 'themis' };
  }
  if (typeNode.kind === ts.SyntaxKind.NumberKeyword) {
    return { inferredType: 'number', sampleValue: 1 };
  }
  if (typeNode.kind === ts.SyntaxKind.BooleanKeyword) {
    return { inferredType: 'boolean', sampleValue: false };
  }
  if (typeNode.kind === ts.SyntaxKind.AnyKeyword || typeNode.kind === ts.SyntaxKind.UnknownKeyword) {
    return { inferredType: 'unknown', sampleValue: undefined };
  }
  if (ts.isArrayTypeNode(typeNode)) {
    return { inferredType: 'array', sampleValue: [] };
  }
  if (ts.isTypeLiteralNode(typeNode)) {
    return {
      inferredType: 'object',
      sampleValue: buildObjectSample(readTypeMembers(typeNode.members, typeIndex))
    };
  }
  if (ts.isLiteralTypeNode(typeNode)) {
    return {
      inferredType: typeof typeNode.literal.text === 'string' ? 'string' : 'literal',
      sampleValue: readLiteralNodeValue(typeNode.literal)
    };
  }
  if (ts.isUnionTypeNode(typeNode)) {
    for (const innerType of typeNode.types) {
      const descriptor = resolveTypeDescriptor(innerType, typeIndex);
      if (descriptor.sampleValue !== undefined) {
        return descriptor;
      }
    }
    return { inferredType: 'unknown', sampleValue: undefined };
  }
  if (ts.isTypeReferenceNode(typeNode) && ts.isIdentifier(typeNode.typeName)) {
    const referenceName = typeNode.typeName.text;
    if (referenceName === 'Array') {
      return { inferredType: 'array', sampleValue: [] };
    }
    if (referenceName === 'Record' || referenceName === 'Readonly' || referenceName === 'Partial') {
      return { inferredType: 'object', sampleValue: {} };
    }
    if (referenceName === 'Promise') {
      return { inferredType: 'promise', sampleValue: undefined };
    }

    const fields = typeIndex.get(referenceName);
    if (fields) {
      return {
        inferredType: 'object',
        sampleValue: buildObjectSample(fields),
        objectFields: fields
      };
    }
  }

  return { inferredType: 'unknown', sampleValue: undefined };
}

function collectExports(statement, state, typeIndex) {
  if (ts.isFunctionDeclaration(statement) || ts.isClassDeclaration(statement) || ts.isEnumDeclaration(statement)) {
    collectNamedRuntimeExport(statement, state, typeIndex);
    return;
  }

  if (ts.isVariableStatement(statement)) {
    collectVariableExports(statement, state, typeIndex);
    return;
  }

  if (ts.isExportAssignment(statement)) {
    state.hasRuntimeExports = true;
    addExportEntry(state, buildExportEntry({
      exportedName: 'default',
      declarationKind: 'default',
      runtimeKind: describeNodeKind(statement.expression),
      functionInfo: analyzeRuntimeFunction(statement.expression, typeIndex),
      localName: readLocalName(statement.expression)
    }));
    return;
  }

  if (ts.isExportDeclaration(statement)) {
    collectExportDeclaration(statement, state);
    return;
  }

  if (ts.isExpressionStatement(statement)) {
    collectCommonJsExports(statement.expression, state, typeIndex);
  }
}

function collectNamedRuntimeExport(statement, state, typeIndex) {
  if (!hasModifier(statement, ts.SyntaxKind.ExportKeyword)) {
    return;
  }

  state.hasRuntimeExports = true;
  const declarationKind = ts.isClassDeclaration(statement)
    ? 'class'
    : ts.isEnumDeclaration(statement)
      ? 'enum'
      : 'function';
  const runtimeKind = declarationKind === 'enum' ? 'object' : declarationKind;
  const exportedName = hasModifier(statement, ts.SyntaxKind.DefaultKeyword)
    ? 'default'
    : statement.name
      ? statement.name.text
      : null;

  if (!exportedName) {
    state.exactExports = false;
    return;
  }

  addExportEntry(state, buildExportEntry({
    exportedName,
    declarationKind,
    runtimeKind,
    functionInfo: analyzeRuntimeFunction(statement, typeIndex),
    localName: statement.name ? statement.name.text : null
  }));
}

function collectVariableExports(statement, state, typeIndex) {
  if (!hasModifier(statement, ts.SyntaxKind.ExportKeyword)) {
    return;
  }

  state.hasRuntimeExports = true;

  for (const declaration of statement.declarationList.declarations) {
    if (!ts.isIdentifier(declaration.name)) {
      state.exactExports = false;
      continue;
    }

    addExportEntry(state, buildExportEntry({
      exportedName: declaration.name.text,
      declarationKind: 'variable',
      runtimeKind: describeNodeKind(declaration.initializer),
      functionInfo: analyzeRuntimeFunction(declaration.initializer, typeIndex),
      localName: declaration.name.text
    }));
  }
}

function collectExportDeclaration(statement, state) {
  if (statement.isTypeOnly) {
    return;
  }

  state.hasRuntimeExports = true;

  if (!statement.exportClause) {
    state.exactExports = false;
    return;
  }

  if (ts.isNamedExports(statement.exportClause)) {
    for (const element of statement.exportClause.elements) {
      if (element.isTypeOnly) {
        continue;
      }

      addExportEntry(state, buildExportEntry({
        exportedName: element.name.text,
        declarationKind: 'reexport',
        runtimeKind: 'unknown',
        functionInfo: null,
        localName: element.propertyName ? element.propertyName.text : element.name.text
      }));
    }
    return;
  }

  state.exactExports = false;
}

function collectCommonJsExports(expression, state, typeIndex) {
  if (!ts.isBinaryExpression(expression) || expression.operatorToken.kind !== ts.SyntaxKind.EqualsToken) {
    return;
  }

  const { left, right } = expression;

  if (isModuleExportsReference(left)) {
    state.hasRuntimeExports = true;

    if (ts.isObjectLiteralExpression(right)) {
      const names = readObjectLiteralExportEntries(right, typeIndex);
      if (!names) {
        state.exactExports = false;
        return;
      }

      for (const entry of names) {
        addExportEntry(state, entry);
      }
      return;
    }

    addExportEntry(state, buildExportEntry({
      exportedName: 'default',
      declarationKind: 'commonjs',
      runtimeKind: describeNodeKind(right),
      functionInfo: analyzeRuntimeFunction(right, typeIndex),
      localName: readLocalName(right)
    }));
    return;
  }

  const namedExport = resolveCommonJsNamedExport(left);
  if (!namedExport) {
    return;
  }

  state.hasRuntimeExports = true;
  addExportEntry(state, buildExportEntry({
    exportedName: namedExport,
    declarationKind: 'commonjs',
    runtimeKind: describeNodeKind(right),
    functionInfo: analyzeRuntimeFunction(right, typeIndex),
    localName: readLocalName(right)
  }));
}

function analyzeRuntimeFunction(node, typeIndex) {
  if (!node) {
    return null;
  }

  if (
    ts.isFunctionDeclaration(node)
    || ts.isFunctionExpression(node)
    || ts.isArrowFunction(node)
    || ts.isMethodDeclaration(node)
    || ts.isGetAccessorDeclaration(node)
    || ts.isSetAccessorDeclaration(node)
  ) {
    return analyzeFunctionLike(node, typeIndex);
  }

  if (ts.isClassDeclaration(node) || ts.isClassExpression(node)) {
    const constructorDeclaration = node.members.find((member) => ts.isConstructorDeclaration(member));
    return {
      async: false,
      parameterCount: constructorDeclaration ? constructorDeclaration.parameters.length : 0,
      parameters: constructorDeclaration ? constructorDeclaration.parameters.map((parameter, index) => analyzeParameter(parameter, typeIndex, index)) : [],
      usesJsx: false,
      classLike: true,
      instanceMethods: node.members
        .filter((member) => ts.isMethodDeclaration(member) && member.name)
        .map((member) => getPropertyName(member.name))
        .filter(Boolean)
    };
  }

  return null;
}

function analyzeFunctionLike(node, typeIndex) {
  return {
    async: hasModifier(node, ts.SyntaxKind.AsyncKeyword),
    parameterCount: node.parameters.length,
    parameters: node.parameters.map((parameter, index) => analyzeParameter(parameter, typeIndex, index)),
    usesJsx: containsJsx(node.body || node),
    eventHandlers: collectJsxEventHandlers(node.body || node),
    instanceMethods: []
  };
}

function analyzeParameter(parameter, typeIndex, index) {
  const typeDescriptor = resolveTypeDescriptor(parameter.type, typeIndex);
  const bindingKind = ts.isObjectBindingPattern(parameter.name)
    ? 'object'
    : ts.isArrayBindingPattern(parameter.name)
      ? 'array'
      : 'identifier';
  const name = readBindingName(parameter.name, index);
  const objectFields = ts.isObjectBindingPattern(parameter.name)
    ? readBindingPatternFields(parameter.name, typeDescriptor.objectFields || [])
    : typeDescriptor.objectFields || [];

  return {
    name,
    bindingKind,
    optional: Boolean(parameter.questionToken),
    hasDefault: Boolean(parameter.initializer),
    defaultValue: parameter.initializer ? serializeLiteralNode(parameter.initializer) : undefined,
    inferredType: typeDescriptor.inferredType,
    sampleValue: typeDescriptor.sampleValue,
    objectFields
  };
}

function readBindingPatternFields(pattern, typeFields) {
  const typeFieldMap = new Map(typeFields.map((field) => [field.name, field]));
  const fields = [];

  for (const element of pattern.elements) {
    if (ts.isOmittedExpression(element)) {
      continue;
    }

    const propertyName = element.propertyName ? getPropertyName(element.propertyName) : getPropertyName(element.name);
    const fieldName = propertyName || getPropertyName(element.name);
    const typeField = typeFieldMap.get(fieldName) || null;

    fields.push({
      name: fieldName,
      optional: Boolean(element.questionToken) || Boolean(typeField && typeField.optional),
      sampleValue: element.initializer
        ? serializeLiteralNode(element.initializer)
        : typeField
          ? cloneSerializable(typeField.sampleValue)
          : undefined,
      inferredType: typeField ? typeField.inferredType : 'unknown'
    });
  }

  for (const field of typeFields) {
    if (!fields.some((candidate) => candidate.name === field.name)) {
      fields.push({
        name: field.name,
        optional: field.optional,
        sampleValue: cloneSerializable(field.sampleValue),
        inferredType: field.inferredType
      });
    }
  }

  return fields;
}

function containsJsx(node) {
  let found = false;

  walkNode(node, (current) => {
    if (
      ts.isJsxElement(current)
      || ts.isJsxSelfClosingElement(current)
      || ts.isJsxFragment(current)
    ) {
      found = true;
      return true;
    }
    return false;
  });

  return found;
}

function collectJsxEventHandlers(node) {
  const handlers = new Set();

  walkNode(node, (current) => {
    if (!ts.isJsxAttribute(current) || !current.name) {
      return false;
    }

    const attributeName = current.name.text;
    if (!/^on[A-Z]/.test(attributeName)) {
      return false;
    }

    handlers.add(attributeName);
    return false;
  });

  return [...handlers].sort((left, right) => {
    const leftIndex = INTERACTION_EVENT_PRIORITY.indexOf(left);
    const rightIndex = INTERACTION_EVENT_PRIORITY.indexOf(right);
    if (leftIndex !== -1 || rightIndex !== -1) {
      const safeLeft = leftIndex === -1 ? INTERACTION_EVENT_PRIORITY.length : leftIndex;
      const safeRight = rightIndex === -1 ? INTERACTION_EVENT_PRIORITY.length : rightIndex;
      if (safeLeft !== safeRight) {
        return safeLeft - safeRight;
      }
    }
    return left.localeCompare(right);
  });
}

function walkNode(node, visit) {
  if (!node || visit(node) === true) {
    return;
  }

  ts.forEachChild(node, (child) => {
    walkNode(child, visit);
  });
}

function buildExportEntry(entry) {
  return {
    exportedName: entry.exportedName,
    declarationKind: entry.declarationKind,
    runtimeKind: entry.runtimeKind || 'unknown',
    functionInfo: entry.functionInfo || null,
    eventHandlers: entry.functionInfo && Array.isArray(entry.functionInfo.eventHandlers) ? [...entry.functionInfo.eventHandlers] : [],
    instanceMethods: entry.functionInfo && Array.isArray(entry.functionInfo.instanceMethods) ? [...entry.functionInfo.instanceMethods] : [],
    localName: entry.localName || null
  };
}

function describeNodeKind(node) {
  if (!node) {
    return 'unknown';
  }
  if (
    ts.isFunctionDeclaration(node)
    || ts.isFunctionExpression(node)
    || ts.isArrowFunction(node)
    || ts.isMethodDeclaration(node)
  ) {
    return 'function';
  }
  if (ts.isClassDeclaration(node) || ts.isClassExpression(node)) {
    return 'class';
  }
  if (ts.isObjectLiteralExpression(node)) {
    return 'object';
  }
  if (ts.isArrayLiteralExpression(node)) {
    return 'array';
  }
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) {
    return 'string';
  }
  if (ts.isNumericLiteral(node)) {
    return 'number';
  }
  if (node.kind === ts.SyntaxKind.TrueKeyword || node.kind === ts.SyntaxKind.FalseKeyword) {
    return 'boolean';
  }
  return 'unknown';
}

function readLocalName(node) {
  if (!node) {
    return null;
  }

  if (ts.isIdentifier(node)) {
    return node.text;
  }
  if ((ts.isFunctionDeclaration(node) || ts.isFunctionExpression(node) || ts.isClassDeclaration(node) || ts.isClassExpression(node)) && node.name) {
    return node.name.text;
  }
  return null;
}

function hasModifier(node, kind) {
  return Array.isArray(node.modifiers) && node.modifiers.some((modifier) => modifier.kind === kind);
}

function isModuleExportsReference(node) {
  return ts.isPropertyAccessExpression(node)
    && ts.isIdentifier(node.expression)
    && node.expression.text === 'module'
    && node.name.text === 'exports';
}

function resolveCommonJsNamedExport(node) {
  const directExports = readExportsMemberName(node);
  if (directExports && directExports !== '__esModule') {
    return directExports;
  }

  if (ts.isPropertyAccessExpression(node) && isModuleExportsReference(node.expression)) {
    return node.name.text === '__esModule' ? null : node.name.text;
  }

  if (ts.isElementAccessExpression(node) && isModuleExportsReference(node.expression)) {
    return readKnownPropertyName(node.argumentExpression);
  }

  return null;
}

function readExportsMemberName(node) {
  if (ts.isPropertyAccessExpression(node) && ts.isIdentifier(node.expression) && node.expression.text === 'exports') {
    return node.name.text;
  }

  if (ts.isElementAccessExpression(node) && ts.isIdentifier(node.expression) && node.expression.text === 'exports') {
    return readKnownPropertyName(node.argumentExpression);
  }

  return null;
}

function readKnownPropertyName(node) {
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) {
    return node.text === '__esModule' ? null : node.text;
  }
  return null;
}

function readModuleSpecifier(node) {
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) {
    return node.text;
  }
  return null;
}

function readObjectLiteralExportEntries(node, typeIndex) {
  const entries = [];

  for (const property of node.properties) {
    if (ts.isSpreadAssignment(property)) {
      return null;
    }

    if (ts.isShorthandPropertyAssignment(property)) {
      entries.push(buildExportEntry({
        exportedName: property.name.text,
        declarationKind: 'commonjs',
        runtimeKind: 'unknown',
        functionInfo: null,
        localName: property.name.text
      }));
      continue;
    }

    if (
      ts.isPropertyAssignment(property)
      || ts.isMethodDeclaration(property)
      || ts.isGetAccessorDeclaration(property)
      || ts.isSetAccessorDeclaration(property)
    ) {
      const propertyName = getPropertyName(property.name);
      if (!propertyName) {
        return null;
      }

      const valueNode = ts.isPropertyAssignment(property) ? property.initializer : property;
      entries.push(buildExportEntry({
        exportedName: propertyName,
        declarationKind: 'commonjs',
        runtimeKind: describeNodeKind(valueNode),
        functionInfo: analyzeRuntimeFunction(valueNode, typeIndex),
        localName: propertyName
      }));
      continue;
    }

    return null;
  }

  return entries.filter((entry) => entry.exportedName !== '__esModule');
}

function getPropertyName(node) {
  if (ts.isIdentifier(node) || ts.isStringLiteral(node) || ts.isNumericLiteral(node)) {
    return node.text;
  }
  return null;
}

function addExportEntry(state, entry) {
  if (!entry || !entry.exportedName || entry.exportedName === '__esModule') {
    return;
  }

  state.exportNames.add(entry.exportedName);
  state.exportMap.set(entry.exportedName, entry);
}

function applyHintExportFilters(analysis) {
  const hints = analysis.hints;
  if (!hints) {
    return;
  }

  let entries = analysis.exportEntries;
  if (Array.isArray(hints.includeExports) && hints.includeExports.length > 0) {
    const includeSet = new Set(hints.includeExports);
    entries = entries.filter((entry) => includeSet.has(entry.exportedName));
  }
  if (Array.isArray(hints.excludeExports) && hints.excludeExports.length > 0) {
    const excludeSet = new Set(hints.excludeExports);
    entries = entries.filter((entry) => !excludeSet.has(entry.exportedName));
  }

  analysis.exportEntries = entries;
  analysis.exportNames = entries.map((entry) => entry.exportedName).sort();
  analysis.hasRuntimeExports = entries.length > 0;
  if (!analysis.hasRuntimeExports) {
    analysis.skipReason = 'Runtime exports were filtered out by hint configuration.';
  }
}

function matchesExportFilter(analysis, matchExport) {
  return analysis.exportNames.some((name) => matchExport.test(name));
}

function resolveScenarioPlans(analysis) {
  const scenarios = [];
  scenarios.push(buildModuleContractScenario(analysis));

  const nextAppComponents = resolveNextAppComponentCases(analysis);
  if (nextAppComponents.length > 0) {
    scenarios.push({
      kind: 'next-app-component',
      confidence: pickHighestConfidence(nextAppComponents.map((entry) => entry.confidence)),
      exports: nextAppComponents.map((entry) => entry.exportName),
      cases: nextAppComponents
    });
  }

  const nextRouteHandlers = resolveNextRouteCases(analysis);
  if (nextRouteHandlers.length > 0) {
    scenarios.push({
      kind: 'next-route-handler',
      confidence: pickHighestConfidence(nextRouteHandlers.map((entry) => entry.confidence)),
      exports: nextRouteHandlers.map((entry) => entry.exportName),
      cases: nextRouteHandlers
    });
  }

  const reservedComponentExports = new Set(nextAppComponents.map((entry) => entry.exportName));
  const reservedRouteExports = new Set(nextRouteHandlers.map((entry) => entry.exportName));

  const reactComponents = resolveReactComponentCases(analysis, reservedComponentExports);
  if (reactComponents.length > 0) {
    scenarios.push({
      kind: 'react-component',
      confidence: pickHighestConfidence(reactComponents.map((entry) => entry.confidence)),
      exports: reactComponents.map((entry) => entry.exportName),
      cases: reactComponents
    });
  }

  const reactHooks = resolveReactHookCases(analysis);
  if (reactHooks.length > 0) {
    scenarios.push({
      kind: 'react-hook',
      confidence: pickHighestConfidence(reactHooks.map((entry) => entry.confidence)),
      exports: reactHooks.map((entry) => entry.exportName),
      cases: reactHooks
    });
  }

  const routeHandlers = resolveRouteCases(analysis, reservedRouteExports);
  if (routeHandlers.length > 0) {
    scenarios.push({
      kind: 'route-handler',
      confidence: pickHighestConfidence(routeHandlers.map((entry) => entry.confidence)),
      exports: routeHandlers.map((entry) => entry.exportName),
      cases: routeHandlers
    });
  }

  const nodeServices = resolveNodeServiceCases(analysis, scenarios);
  if (nodeServices.length > 0) {
    scenarios.push({
      kind: 'node-service',
      confidence: pickHighestConfidence(nodeServices.map((entry) => entry.confidence)),
      exports: nodeServices.map((entry) => entry.exportName),
      cases: nodeServices
    });
  }

  return scenarios.sort((a, b) => SCENARIO_PRIORITY.indexOf(a.kind) - SCENARIO_PRIORITY.indexOf(b.kind));
}

function buildModuleContractScenario(analysis) {
  return {
    kind: 'module-contract',
    confidence: analysis.exactExports ? 'medium' : 'low',
    exports: [...analysis.exportNames],
    cases: []
  };
}

function resolveNextAppComponentCases(analysis) {
  if (!isNextAppComponentFile(analysis.file)) {
    return [];
  }

  const forced = hasScenarioHint(analysis, 'next-app-component');
  const cases = [];

  for (const entry of analysis.exportEntries) {
    if (!entry.functionInfo && entry.runtimeKind !== 'class') {
      continue;
    }

    if (!forced && !isNextAppEntry(entry)) {
      continue;
    }

    const propsSamples = resolveNextAppPropsSamples(analysis, entry);
    cases.push({
      exportName: entry.exportedName,
      displayName: resolveDisplayExportName(analysis, entry),
      caseName: propsSamples.hinted ? 'renders a hinted next app contract' : 'renders an inferred next app contract',
      props: propsSamples.props,
      interactions: resolveComponentInteractionSamples(analysis, entry),
      confidence: propsSamples.confidence
    });
  }

  return cases;
}

function resolveReactComponentCases(analysis, reservedExports = new Set()) {
  const forced = hasScenarioHint(analysis, 'react-component');
  const cases = [];

  for (const entry of analysis.exportEntries) {
    if (reservedExports.has(entry.exportedName)) {
      continue;
    }

    if (!entry.functionInfo && entry.runtimeKind !== 'class') {
      continue;
    }

    const looksLikeComponent = forced
      || (analysis.usesJsx || analysis.importsReact)
      && !isHookName(entry.exportedName)
      && isPascalCase(resolveDisplayExportName(analysis, entry))
      && (entry.functionInfo ? entry.functionInfo.usesJsx || analysis.usesJsx : true);

    if (!looksLikeComponent) {
      continue;
    }

    const propsSamples = resolveComponentPropsSamples(analysis, entry);
    cases.push({
      exportName: entry.exportedName,
      displayName: resolveDisplayExportName(analysis, entry),
      caseName: propsSamples.hinted ? 'renders with hinted props' : 'renders with inferred props',
      props: propsSamples.props,
      interactions: resolveComponentInteractionSamples(analysis, entry),
      confidence: propsSamples.confidence
    });
  }

  return cases;
}

function resolveReactHookCases(analysis) {
  const forced = hasScenarioHint(analysis, 'react-hook');
  const cases = [];

  for (const entry of analysis.exportEntries) {
    if (!entry.functionInfo) {
      continue;
    }

    if (!forced && !isHookName(resolveDisplayExportName(analysis, entry))) {
      continue;
    }

    const argsSamples = resolveArgsSamples(analysis, entry, 'hookArgs');
    cases.push({
      exportName: entry.exportedName,
      displayName: resolveDisplayExportName(analysis, entry),
      caseName: argsSamples.hinted ? 'returns a hinted hook contract' : 'returns an inferred hook contract',
      args: argsSamples.args,
      interactions: resolveHookInteractionSamples(analysis, entry),
      confidence: argsSamples.confidence
    });
  }

  return cases;
}

function resolveNextRouteCases(analysis) {
  if (!isNextRouteFile(analysis.file)) {
    return [];
  }

  const forced = hasScenarioHint(analysis, 'next-route-handler');
  const cases = [];

  for (const entry of analysis.exportEntries) {
    if (!entry.functionInfo) {
      continue;
    }

    if (!forced && !HTTP_METHOD_EXPORTS.includes(entry.exportedName)) {
      continue;
    }

    const requestHints = resolveHintCollection(analysis.hints, 'routeRequests', entry.exportedName);
    const contextHints = resolveHintCollection(analysis.hints, 'routeContext', entry.exportedName);
    const request = normalizeRouteRequestHint(requestHints, entry.exportedName, analysis.relativeFile);
    const context = normalizeRouteContextHint(contextHints);

    cases.push({
      exportName: entry.exportedName,
      displayName: resolveDisplayExportName(analysis, entry),
      caseName: requestHints ? 'returns a hinted next route contract' : 'returns an inferred next route contract',
      request,
      context,
      confidence: requestHints ? resolveHintConfidence(analysis) : 'medium'
    });
  }

  return cases;
}

function resolveRouteCases(analysis, reservedExports = new Set()) {
  const forced = hasScenarioHint(analysis, 'route-handler');
  const cases = [];

  for (const entry of analysis.exportEntries) {
    if (reservedExports.has(entry.exportedName)) {
      continue;
    }

    if (!entry.functionInfo) {
      continue;
    }

    const looksLikeRoute = forced
      || HTTP_METHOD_EXPORTS.includes(entry.exportedName)
      || isRouteFile(analysis.file) && entry.exportedName === 'default';

    if (!looksLikeRoute) {
      continue;
    }

    const requestHints = resolveHintCollection(analysis.hints, 'routeRequests', entry.exportedName);
    const contextHints = resolveHintCollection(analysis.hints, 'routeContext', entry.exportedName);
    const request = normalizeRouteRequestHint(requestHints, entry.exportedName, analysis.relativeFile);
    const context = normalizeRouteContextHint(contextHints);

    cases.push({
      exportName: entry.exportedName,
      displayName: resolveDisplayExportName(analysis, entry),
      caseName: requestHints ? 'returns a hinted response contract' : 'returns an inferred response contract',
      request,
      context,
      confidence: requestHints ? resolveHintConfidence(analysis) : 'medium'
    });
  }

  return cases;
}

function resolveNodeServiceCases(analysis, existingScenarios) {
  const reservedExports = new Set();
  for (const scenario of existingScenarios) {
    if (scenario.kind === 'module-contract') {
      continue;
    }
    for (const exportName of scenario.exports) {
      reservedExports.add(exportName);
    }
  }

  const forced = hasScenarioHint(analysis, 'node-service');
  const cases = [];

  for (const entry of analysis.exportEntries) {
    if (!entry.functionInfo || reservedExports.has(entry.exportedName)) {
      continue;
    }

    if (!forced && entry.runtimeKind !== 'function') {
      continue;
    }

    const argsSamples = resolveArgsSamples(analysis, entry, 'serviceArgs');
    cases.push({
      exportName: entry.exportedName,
      displayName: resolveDisplayExportName(analysis, entry),
      caseName: argsSamples.hinted ? 'returns a hinted invocation contract' : 'returns an inferred invocation contract',
      args: argsSamples.args,
      confidence: argsSamples.confidence
    });
  }

  return cases;
}

function resolveNextAppPropsSamples(analysis, entry) {
  const hinted = resolveHintCollection(analysis.hints, 'componentProps', entry.exportedName);
  if (hinted) {
    const props = Array.isArray(hinted) ? hinted[0] : hinted;
    return {
      props: cloneSerializable(props) || {},
      hinted: true,
      confidence: resolveHintConfidence(analysis)
    };
  }

  const base = resolveComponentPropsSamples(analysis, entry);
  const props = isPlainObject(base.props) ? { ...base.props } : {};

  if (!Object.prototype.hasOwnProperty.call(props, 'params')) {
    props.params = {};
  }
  if (!Object.prototype.hasOwnProperty.call(props, 'searchParams')) {
    props.searchParams = {};
  }
  if (path.parse(analysis.file).name.toLowerCase() === 'layout' && !Object.prototype.hasOwnProperty.call(props, 'children')) {
    props.children = 'Themis child';
  }

  return {
    props,
    hinted: base.hinted,
    confidence: base.confidence === 'low' ? 'medium' : base.confidence
  };
}

function resolveComponentPropsSamples(analysis, entry) {
  const hinted = resolveHintCollection(analysis.hints, 'componentProps', entry.exportedName);
  if (hinted) {
    const props = Array.isArray(hinted) ? hinted[0] : hinted;
    return {
      props: cloneSerializable(props) || {},
      hinted: true,
      confidence: resolveHintConfidence(analysis)
    };
  }

  const functionInfo = entry.functionInfo || { parameters: [] };
  const firstParameter = functionInfo.parameters[0];
  if (!firstParameter) {
    return { props: {}, hinted: false, confidence: 'medium' };
  }

  if (firstParameter.bindingKind === 'object') {
    return {
      props: buildObjectSample(firstParameter.objectFields),
      hinted: false,
      confidence: 'medium'
    };
  }

  if (firstParameter.sampleValue && isPlainObject(firstParameter.sampleValue)) {
    return {
      props: cloneSerializable(firstParameter.sampleValue),
      hinted: false,
      confidence: 'medium'
    };
  }

  return {
    props: {},
    hinted: false,
    confidence: 'low'
  };
}

function resolveArgsSamples(analysis, entry, hintKey) {
  const hinted = resolveHintCollection(analysis.hints, hintKey, entry.exportedName);
  if (hinted) {
    const args = Array.isArray(hinted) && Array.isArray(hinted[0]) ? hinted[0] : Array.isArray(hinted) ? hinted : [hinted];
    return {
      args: cloneSerializable(args),
      hinted: true,
      confidence: resolveHintConfidence(analysis)
    };
  }

  const functionInfo = entry.functionInfo || { parameters: [] };
  const args = functionInfo.parameters.map((parameter, index) => inferSampleFromParameter(parameter, entry, index));
  return {
    args,
    hinted: false,
    confidence: args.length === 0 ? 'medium' : 'low'
  };
}

function resolveComponentInteractionSamples(analysis, entry) {
  const hinted = resolveHintCollection(analysis.hints, 'componentInteractions', entry.exportedName);
  if (Array.isArray(hinted)) {
    return cloneSerializable(hinted);
  }

  return inferComponentInteractionPlan(entry);
}

function resolveHookInteractionSamples(analysis, entry) {
  const hinted = resolveHintCollection(analysis.hints, 'hookInteractions', entry.exportedName);
  if (Array.isArray(hinted)) {
    return cloneSerializable(hinted);
  }

  return inferHookInteractionPlan(entry);
}

function inferComponentInteractionPlan(entry) {
  const events = Array.isArray(entry.eventHandlers) ? entry.eventHandlers : [];
  return [...new Set(events)]
    .slice(0, 4)
    .map((eventName) => ({ event: eventName, repeat: 1 }));
}

function inferHookInteractionPlan(entry) {
  const methods = Array.isArray(entry.instanceMethods) ? entry.instanceMethods : [];
  return [...new Set(methods.filter((methodName) => STATEFUL_METHOD_PRIORITY.includes(methodName)))]
    .slice(0, 4)
    .map((methodName) => ({ method: methodName, repeat: 1 }));
}

function inferSampleFromParameter(parameter, entry, index) {
  if (parameter.defaultValue !== undefined) {
    return cloneSerializable(parameter.defaultValue);
  }

  if (parameter.bindingKind === 'object') {
    return buildObjectSample(parameter.objectFields);
  }

  if (parameter.sampleValue !== undefined) {
    return cloneSerializable(parameter.sampleValue);
  }

  const lowerName = String(parameter.name || '').toLowerCase();
  const entryName = String(entry.localName || entry.exportedName || '').toLowerCase();

  if (
    parameter.inferredType === 'number'
    || /(count|size|total|limit|offset|page|index|amount|id)$/.test(lowerName)
    || /(add|subtract|multiply|divide|sum|total|count)/.test(entryName)
  ) {
    return index + 1;
  }

  if (parameter.inferredType === 'boolean' || /^(is|has|can|should|enabled|disabled)/.test(lowerName)) {
    return false;
  }

  if (parameter.inferredType === 'array') {
    return [];
  }

  if (parameter.inferredType === 'object' || /(options|config|params|payload|input|props|context)$/.test(lowerName)) {
    return {};
  }

  if (/(url|href|uri)/.test(lowerName)) {
    return 'https://themis.test';
  }

  if (lowerName === 'children') {
    return 'Themis child';
  }

  return index === 0 ? 'themis' : `themis-${index + 1}`;
}

function buildObjectSample(fields) {
  const sample = {};

  for (const field of fields || []) {
    const inferred = field.sampleValue !== undefined
      ? cloneSerializable(field.sampleValue)
      : inferFieldFallback(field.name, field.inferredType);
    sample[field.name] = inferred;
  }

  return sample;
}

function inferFieldFallback(name, inferredType) {
  const lowerName = String(name || '').toLowerCase();

  if (inferredType === 'number' || /(count|size|total|limit|offset|page|index|amount|id)$/.test(lowerName)) {
    return 1;
  }
  if (inferredType === 'boolean' || /^(is|has|can|should|enabled|disabled)/.test(lowerName)) {
    return false;
  }
  if (inferredType === 'array') {
    return [];
  }
  if (inferredType === 'object' || /(options|config|params|payload|input|props|context)$/.test(lowerName)) {
    return {};
  }
  if (lowerName === 'children') {
    return 'Themis child';
  }
  return 'themis';
}

function hasScenarioHint(analysis, scenarioName) {
  const scenarios = analysis.hints && analysis.hints.scenarios;
  return Array.isArray(scenarios) && scenarios.includes(scenarioName);
}

function resolveHintCollection(hints, key, exportName) {
  if (!hints || !hints[key]) {
    return null;
  }

  const value = hints[key];
  if (Array.isArray(value)) {
    return value;
  }
  if (isPlainObject(value)) {
    if (Object.prototype.hasOwnProperty.call(value, exportName)) {
      return value[exportName];
    }
    if (Object.prototype.hasOwnProperty.call(value, 'default')) {
      return value.default;
    }
  }
  return value;
}

function normalizeRouteRequestHint(value, exportName, relativeFile) {
  if (value) {
    const hint = Array.isArray(value) ? value[0] : value;
    return {
      method: hint.method || exportName || 'GET',
      url: hint.url || `https://themis.test/${relativeFile.replace(/^src\//, '').replace(/\.(jsx?|tsx?)$/, '')}`,
      headers: cloneSerializable(hint.headers) || {},
      body: hint.body,
      json: cloneSerializable(hint.json)
    };
  }

  return {
    method: exportName || 'GET',
    url: `https://themis.test/${relativeFile.replace(/^src\//, '').replace(/\.(jsx?|tsx?)$/, '')}`,
    headers: {},
    body: undefined,
    json: undefined
  };
}

function normalizeRouteContextHint(value) {
  if (!value) {
    return { params: {} };
  }

  const hint = Array.isArray(value) ? value[0] : value;
  return cloneSerializable(hint) || { params: {} };
}

function resolveHintConfidence(analysis) {
  return analysis.hintOrigin === 'scaffold' ? 'medium' : 'high';
}

function resolveHintOrigin(hints) {
  return isScaffoldHintPayload(hints) ? 'scaffold' : 'user';
}

function isScaffoldHintPayload(hints) {
  return Boolean(
    hints
    && isPlainObject(hints.$meta)
    && hints.$meta.generatedBy === SCAFFOLD_HINT_META.generatedBy
    && hints.$meta.kind === SCAFFOLD_HINT_META.kind
  );
}

function planHintScaffold(analysis, projectRoot) {
  const hintFile = resolveHintFilePath(analysis.file);
  const scaffold = buildHintScaffold(analysis);
  const existingHints = analysis.sidecarHints;

  if (!scaffold) {
    return null;
  }

  if (!existingHints) {
    return {
      file: hintFile,
      action: 'create',
      payload: scaffold
    };
  }

  if (!isScaffoldHintPayload(existingHints)) {
    return null;
  }

  const merged = mergeScaffoldHints(existingHints, scaffold);
  return {
    file: hintFile,
    action: serializeHintPayload(existingHints) === serializeHintPayload(merged) ? 'unchanged' : 'update',
    payload: merged
  };
}

function buildHintScaffold(analysis) {
  const scaffold = {
    $meta: {
      ...SCAFFOLD_HINT_META,
      sourceFile: normalizePath(analysis.relativeFile || analysis.file)
    }
  };

  const nextAppCases = resolveNextAppComponentCases(analysis);
  const nextRouteCases = resolveNextRouteCases(analysis);
  const reservedComponentExports = new Set(nextAppCases.map((entry) => entry.exportName));
  const reservedRouteExports = new Set(nextRouteCases.map((entry) => entry.exportName));
  const componentCases = resolveReactComponentCases(analysis, reservedComponentExports);
  const hookCases = resolveReactHookCases(analysis);
  const routeCases = resolveRouteCases(analysis, reservedRouteExports);
  const reservedScenarios = [buildModuleContractScenario(analysis)];
  if (nextAppCases.length > 0) {
    reservedScenarios.push({
      kind: 'next-app-component',
      exports: nextAppCases.map((entry) => entry.exportName)
    });
  }
  if (nextRouteCases.length > 0) {
    reservedScenarios.push({
      kind: 'next-route-handler',
      exports: nextRouteCases.map((entry) => entry.exportName)
    });
  }
  if (componentCases.length > 0) {
    reservedScenarios.push({
      kind: 'react-component',
      exports: componentCases.map((entry) => entry.exportName)
    });
  }
  if (hookCases.length > 0) {
    reservedScenarios.push({
      kind: 'react-hook',
      exports: hookCases.map((entry) => entry.exportName)
    });
  }
  if (routeCases.length > 0) {
    reservedScenarios.push({
      kind: 'route-handler',
      exports: routeCases.map((entry) => entry.exportName)
    });
  }
  const serviceCases = resolveNodeServiceCases(analysis, reservedScenarios);
  const scenarioNames = [];

  if (nextAppCases.length > 0) {
    scaffold.componentProps = buildNamedHintMap(nextAppCases, (entry) => [cloneSerializable(entry.props) || {}]);
    scaffold.componentInteractions = buildNamedHintMap(nextAppCases, (entry) => cloneSerializable(entry.interactions) || []);
    scenarioNames.push('next-app-component');
  }

  if (nextRouteCases.length > 0) {
    scaffold.routeRequests = buildNamedHintMap(nextRouteCases, (entry) => [cloneSerializable(entry.request) || {}]);
    scaffold.routeContext = buildNamedHintMap(nextRouteCases, (entry) => [cloneSerializable(entry.context) || { params: {} }]);
    scenarioNames.push('next-route-handler');
  }

  if (componentCases.length > 0) {
    scaffold.componentProps = {
      ...(isPlainObject(scaffold.componentProps) ? scaffold.componentProps : {}),
      ...buildNamedHintMap(componentCases, (entry) => [cloneSerializable(entry.props) || {}])
    };
    scaffold.componentInteractions = {
      ...(isPlainObject(scaffold.componentInteractions) ? scaffold.componentInteractions : {}),
      ...buildNamedHintMap(componentCases, (entry) => cloneSerializable(entry.interactions) || [])
    };
    scenarioNames.push('react-component');
  }

  if (hookCases.length > 0) {
    scaffold.hookArgs = buildNamedHintMap(hookCases, (entry) => [cloneSerializable(entry.args) || []]);
    scaffold.hookInteractions = buildNamedHintMap(hookCases, (entry) => cloneSerializable(entry.interactions) || []);
    scenarioNames.push('react-hook');
  }

  if (routeCases.length > 0) {
    scaffold.routeRequests = {
      ...(isPlainObject(scaffold.routeRequests) ? scaffold.routeRequests : {}),
      ...buildNamedHintMap(routeCases, (entry) => [cloneSerializable(entry.request) || {}])
    };
    scaffold.routeContext = {
      ...(isPlainObject(scaffold.routeContext) ? scaffold.routeContext : {}),
      ...buildNamedHintMap(routeCases, (entry) => [cloneSerializable(entry.context) || { params: {} }])
    };
    scenarioNames.push('route-handler');
  }

  if (serviceCases.length > 0) {
    scaffold.serviceArgs = buildNamedHintMap(serviceCases, (entry) => [cloneSerializable(entry.args) || []]);
    scenarioNames.push('node-service');
  }

  if (scenarioNames.length === 0) {
    return null;
  }

  scaffold.scenarios = [...new Set(scenarioNames)];
  return scaffold;
}

function buildNamedHintMap(entries, resolveValue) {
  const map = {};

  for (const entry of entries) {
    map[entry.exportName] = resolveValue(entry);
  }

  return map;
}

function mergeScaffoldHints(existingHints, scaffold) {
  const merged = cloneSerializable(existingHints) || {};
  merged.$meta = {
    ...SCAFFOLD_HINT_META,
    ...(isPlainObject(existingHints.$meta) ? existingHints.$meta : {}),
    sourceFile: scaffold.$meta.sourceFile
  };

  mergeHintCollection(merged, scaffold, 'componentProps');
  mergeHintCollection(merged, scaffold, 'componentInteractions');
  mergeHintCollection(merged, scaffold, 'hookArgs');
  mergeHintCollection(merged, scaffold, 'hookInteractions');
  mergeHintCollection(merged, scaffold, 'serviceArgs');
  mergeHintCollection(merged, scaffold, 'routeRequests');
  mergeHintCollection(merged, scaffold, 'routeContext');

  const existingScenarios = Array.isArray(merged.scenarios) ? merged.scenarios : [];
  merged.scenarios = [...new Set([...existingScenarios, ...scaffold.scenarios])].sort();

  return merged;
}

function mergeHintCollection(target, scaffold, key) {
  if (!isPlainObject(scaffold[key])) {
    return;
  }

  const existing = isPlainObject(target[key]) ? { ...target[key] } : {};
  for (const [entryKey, entryValue] of Object.entries(scaffold[key])) {
    if (!Object.prototype.hasOwnProperty.call(existing, entryKey)) {
      existing[entryKey] = cloneSerializable(entryValue);
    }
  }
  target[key] = existing;
}

