function flattenScenarioCases(scenarios, kind) {
  const scenario = scenarios.find((candidate) => candidate.kind === kind);
  return scenario && Array.isArray(scenario.cases) ? scenario.cases : [];
}

function writeManagedFile(filePath, content, options = {}) {
  if (fs.existsSync(filePath)) {
    const existing = fs.readFileSync(filePath, 'utf8');
    if (existing === content) {
      return;
    }

    if (!existing.startsWith(GENERATED_MARKER) && !options.force) {
      throw new Error(
        `Refusing to overwrite non-Themis file: ${filePath}. Use --force to replace it.`
      );
    }
  }

  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, 'utf8');
}

function removePlannedFiles(plans, outputDir) {
  const removedFiles = [];

  for (const plan of plans) {
    if (!fs.existsSync(plan.file)) {
      continue;
    }

    const existing = fs.readFileSync(plan.file, 'utf8');
    if (!existing.startsWith(GENERATED_MARKER)) {
      continue;
    }

    fs.rmSync(plan.file, { force: true });
    removedFiles.push(plan.file);
    removeSnapshotFile(plan.file);
    pruneEmptyDirectories(path.dirname(plan.file), outputDir);
  }

  return removedFiles.sort();
}

function cleanupStaleGeneratedFiles(outputDir, expectedManagedFiles, explicitRemoved = []) {
  const removedFiles = [...explicitRemoved];

  if (!fs.existsSync(outputDir)) {
    return removedFiles.sort();
  }

  for (const filePath of walkFiles(outputDir)) {
    const basename = path.basename(filePath);
    if (basename === '.DS_Store') {
      continue;
    }

    const existing = fs.readFileSync(filePath, 'utf8');
    if (!existing.startsWith(GENERATED_MARKER)) {
      continue;
    }

    if (expectedManagedFiles.has(safeRealpath(filePath))) {
      continue;
    }

    fs.rmSync(filePath, { force: true });
    removedFiles.push(filePath);

    if (filePath.endsWith('.generated.test.js')) {
      removeSnapshotFile(filePath);
    }

    pruneEmptyDirectories(path.dirname(filePath), outputDir);
  }

  return [...new Set(removedFiles)].sort();
}

function *walkFiles(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      yield *walkFiles(fullPath);
      continue;
    }
    if (entry.isFile()) {
      yield fullPath;
    }
  }
}

function removeSnapshotFile(testFilePath) {
  const snapshotFile = path.join(
    path.dirname(testFilePath),
    '__snapshots__',
    `${path.basename(testFilePath)}.snapshots.json`
  );
  if (fs.existsSync(snapshotFile)) {
    fs.rmSync(snapshotFile, { force: true });
    pruneEmptyDirectories(path.dirname(snapshotFile), path.dirname(testFilePath));
  }
}

function removeManagedHelper(helperFile, outputDir) {
  if (!fs.existsSync(helperFile)) {
    return false;
  }

  const existing = fs.readFileSync(helperFile, 'utf8');
  if (!existing.startsWith(GENERATED_MARKER)) {
    return false;
  }

  fs.rmSync(helperFile, { force: true });
  pruneEmptyDirectories(path.dirname(helperFile), outputDir);
  return true;
}

function pruneEmptyDirectories(startDir, stopDir) {
  let currentDir = startDir;
  while (isSubPath(currentDir, stopDir) || currentDir === stopDir) {
    if (!fs.existsSync(currentDir)) {
      break;
    }

    if (fs.readdirSync(currentDir).length > 0) {
      break;
    }

    fs.rmdirSync(currentDir);
    if (currentDir === stopDir) {
      break;
    }
    currentDir = path.dirname(currentDir);
  }
}

function serializeLiteralNode(node) {
  if (!node) {
    return undefined;
  }
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) {
    return node.text;
  }
  if (ts.isNumericLiteral(node)) {
    return Number(node.text);
  }
  if (node.kind === ts.SyntaxKind.TrueKeyword) {
    return true;
  }
  if (node.kind === ts.SyntaxKind.FalseKeyword) {
    return false;
  }
  if (node.kind === ts.SyntaxKind.NullKeyword) {
    return null;
  }
  if (ts.isArrayLiteralExpression(node)) {
    return node.elements.map((element) => serializeLiteralNode(element));
  }
  if (ts.isObjectLiteralExpression(node)) {
    const value = {};
    for (const property of node.properties) {
      if (!ts.isPropertyAssignment(property)) {
        return undefined;
      }
      const key = getPropertyName(property.name);
      if (!key) {
        return undefined;
      }
      value[key] = serializeLiteralNode(property.initializer);
    }
    return value;
  }
  return undefined;
}

function readLiteralNodeValue(node) {
  if (!node) {
    return undefined;
  }
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) {
    return node.text;
  }
  if (ts.isNumericLiteral(node)) {
    return Number(node.text);
  }
  if (node.kind === ts.SyntaxKind.TrueKeyword) {
    return true;
  }
  if (node.kind === ts.SyntaxKind.FalseKeyword) {
    return false;
  }
  return undefined;
}

function readBindingName(node, index) {
  if (ts.isIdentifier(node)) {
    return node.text;
  }
  if (ts.isObjectBindingPattern(node)) {
    return `object${index + 1}`;
  }
  if (ts.isArrayBindingPattern(node)) {
    return `array${index + 1}`;
  }
  return `arg${index + 1}`;
}

function cloneSerializable(value) {
  if (value === undefined) {
    return undefined;
  }
  return JSON.parse(JSON.stringify(value));
}

function isPlainObject(value) {
  if (!value || typeof value !== 'object') {
    return false;
  }

  const prototype = Object.getPrototypeOf(value);
  return prototype === Object.prototype || prototype === null;
}

function createSourceHash(source) {
  return crypto.createHash('sha1').update(source).digest('hex');
}

function normalizeRelativeModule(relativePath) {
  const normalized = normalizePath(relativePath);
  return normalized.startsWith('.') ? normalized : `./${normalized}`;
}

function normalizePath(value) {
  return String(value).split(path.sep).join('/');
}

function isSubPath(targetPath, parentPath) {
  const relative = path.relative(parentPath, targetPath);
  return relative !== '' && !relative.startsWith('..') && !path.isAbsolute(relative);
}

function isSameOrSubPath(targetPath, parentPath) {
  return path.resolve(targetPath) === path.resolve(parentPath) || isSubPath(targetPath, parentPath);
}

function safeRealpath(targetPath) {
  try {
    return fs.realpathSync.native(targetPath);
  } catch (error) {
    return path.resolve(targetPath);
  }
}

function formatPathForDisplay(projectRoot, targetPath) {
  const relative = path.relative(projectRoot, targetPath);
  return relative && !relative.startsWith('..') ? normalizePath(relative) : targetPath;
}

