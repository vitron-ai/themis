function resolveCleanTargets({ analyses, existingMap, projectRoot, selectedSourceKeys, hasSelectionFilters, changedSet }) {
  const targets = [];
  const analysisKeys = new Set(analyses.map((analysis) => safeRealpath(analysis.file)));

  for (const entry of existingMap.entries) {
    const entryKey = entry.sourceKey;
    const shouldRemove = hasSelectionFilters
      ? selectedSourceKeys.has(entryKey) || analysisKeys.has(entryKey) || Boolean(changedSet && changedSet.has(entryKey))
      : true;

    if (!shouldRemove) {
      continue;
    }

    targets.push({
      sourceKey: entryKey,
      sourceFile: path.resolve(projectRoot, entry.sourceFile),
      file: path.resolve(projectRoot, entry.testFile),
      reason: 'Clean mode removed generated test.'
    });
  }

  return targets;
}

function mergeGenerateMapEntries({ existingEntries, plans, removedPlans, review, clean, projectRoot }) {
  const merged = new Map();

  for (const entry of existingEntries) {
    merged.set(entry.sourceKey, { ...entry });
  }

  for (const removedPlan of removedPlans) {
    merged.delete(removedPlan.sourceKey);
  }

  if (clean) {
    return [...merged.values()];
  }

  for (const plan of plans) {
    if (plan.action === 'conflict') {
      continue;
    }

    const sourceKey = safeRealpath(plan.sourceFile);
    const existing = merged.get(sourceKey);
    merged.set(sourceKey, {
      ...plan.entry,
      sourceKey,
      sourceFile: formatPathForDisplay(projectRoot, plan.sourceFile),
      testFile: formatPathForDisplay(projectRoot, plan.outputFile),
      previousTestFile: existing ? existing.testFile : null
    });
  }

  if (review) {
    return [...merged.values()];
  }

  return [...merged.values()].sort((a, b) => a.sourceFile.localeCompare(b.sourceFile));
}

