function discoverSourceFiles(dir, outputDir, files) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    if (entry.name === 'node_modules' || entry.name.startsWith('.')) {
      continue;
    }

    const fullPath = path.join(dir, entry.name);
    if (isSameOrSubPath(fullPath, outputDir)) {
      continue;
    }

    if (entry.isDirectory()) {
      discoverSourceFiles(fullPath, outputDir, files);
      continue;
    }

    if (!entry.isFile() || !isEligibleSourceFile(fullPath)) {
      continue;
    }

    files.push(fullPath);
  }

  files.sort();
}

function isEligibleSourceFile(filePath) {
  const extension = path.extname(filePath).toLowerCase();
  if (!SOURCE_EXTENSIONS.has(extension)) {
    return false;
  }
  if (filePath.endsWith('.d.ts')) {
    return false;
  }
  if (filePath.endsWith(HINT_FILE_SUFFIX)) {
    return false;
  }
  return !TEST_FILE_PATTERN.test(path.basename(filePath));
}

function resolveScriptKind(filePath) {
  const extension = path.extname(filePath).toLowerCase();
  if (extension === '.ts') {
    return ts.ScriptKind.TS;
  }
  if (extension === '.tsx') {
    return ts.ScriptKind.TSX;
  }
  if (extension === '.jsx') {
    return ts.ScriptKind.JSX;
  }
  return ts.ScriptKind.JS;
}

