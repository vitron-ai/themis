function generateTestsFromSource(cwd, options = {}) {
  const projectRoot = path.resolve(cwd || process.cwd());
  const normalizedOptions = normalizeGenerateOptions(projectRoot, options);
  const projectProviders = loadProjectProviders(projectRoot);
  const scanTarget = resolveScanTarget(projectRoot, normalizedOptions.targetDir || 'src');
  const outputDir = path.resolve(projectRoot, normalizedOptions.outputDir || path.join('tests', 'generated'));
  const helperFile = path.join(outputDir, GENERATED_HELPER_NAME);
  const mapFile = path.resolve(projectRoot, GENERATED_MAP_ARTIFACT);
  const resultArtifactFile = path.resolve(projectRoot, GENERATED_RESULT_ARTIFACT);
  const handoffArtifactFile = path.resolve(projectRoot, GENERATED_HANDOFF_ARTIFACT);
  const backlogArtifactFile = path.resolve(projectRoot, GENERATED_BACKLOG_ARTIFACT);
  const existingMap = readGenerateMap(mapFile, projectRoot);
  const changedSet = normalizedOptions.changed ? collectChangedPaths(projectRoot) : null;
  const candidateFiles = selectSourceFiles({
    scanTarget,
    outputDir,
    projectRoot,
    options: normalizedOptions,
    changedSet
  });
  const analyses = [];
  const skippedFiles = [];
  const createdHintFiles = [];
  const updatedHintFiles = [];
  const unchangedHintFiles = [];
  const selectedSourceKeys = new Set(candidateFiles.map((file) => safeRealpath(file)));
  const existingEntryMap = new Map(existingMap.entries.map((entry) => [entry.sourceKey, entry]));
  const hasSelectionFilters = hasTargetedSelection(normalizedOptions, scanTarget);

  for (const file of candidateFiles) {
    const sidecarHints = readHintFile(file);
    const analysis = analyzeSourceFile(file);
    analysis.sidecarHints = sidecarHints;
    const matchedProviders = matchProjectProviders(projectProviders, file, projectRoot);
    analysis.projectProviders = matchedProviders;
    analysis.projectProviderFile = projectProviders ? projectProviders.file : null;
    analysis.providerRuntimeIndexes = matchedProviders
      .filter((provider) => typeof provider.applyMocks === 'function' || typeof provider.wrapRender === 'function')
      .map((provider) => provider.index);
    analysis.hints = mergeHintSources(buildProviderHints(matchedProviders), sidecarHints);
    analysis.hintsFile = sidecarHints ? resolveHintFilePath(file) : null;
    analysis.sourceHash = createSourceHash(analysis.source);
    analysis.relativeFile = formatPathForDisplay(projectRoot, file);

    if (normalizedOptions.writeHints) {
      const hintPlan = planHintScaffold(analysis, projectRoot);
      if (hintPlan) {
        if (!normalizedOptions.review && (hintPlan.action === 'create' || hintPlan.action === 'update')) {
          writeHintFile(hintPlan.file, hintPlan.payload);
        }

        if (hintPlan.action === 'create') {
          createdHintFiles.push(hintPlan.file);
        } else if (hintPlan.action === 'update') {
          updatedHintFiles.push(hintPlan.file);
        } else {
          unchangedHintFiles.push(hintPlan.file);
        }

        analysis.hints = hintPlan.payload;
        analysis.hintsFile = hintPlan.file;
      }
    }

    analysis.hintOrigin = analysis.hints ? resolveHintOrigin(analysis.hints) : null;
    applyHintExportFilters(analysis);

    if (!analysis.hasRuntimeExports) {
      skippedFiles.push({
        file,
        reason: analysis.skipReason || 'No runtime exports discovered.',
        stage: 'analysis'
      });
      continue;
    }

    if (normalizedOptions.matchExport && !matchesExportFilter(analysis, normalizedOptions.matchExport)) {
      skippedFiles.push({
        file,
        reason: `No exported runtime symbol matched ${normalizedOptions.matchExport}.`,
        stage: 'filter'
      });
      continue;
    }

    analysis.scenarios = resolveScenarioPlans(analysis);
    analysis.moduleKind = pickPrimaryScenario(analysis.scenarios);
    analysis.confidence = pickOverallConfidence(analysis.scenarios);

    if (normalizedOptions.scenario && !analysis.scenarios.some((scenario) => scenario.kind === normalizedOptions.scenario)) {
      skippedFiles.push({
        file,
        reason: `No scenario matched ${normalizedOptions.scenario}.`,
        stage: 'filter'
      });
      continue;
    }

    if (normalizedOptions.minConfidence && !isConfidenceAtLeast(analysis.confidence, normalizedOptions.minConfidence)) {
      skippedFiles.push({
        file,
        reason: `Confidence ${analysis.confidence} is below ${normalizedOptions.minConfidence}.`,
        stage: 'filter'
      });
      continue;
    }

    analysis.outputFile = resolveGeneratedTestPath(outputDir, scanTarget.mirrorBase, analysis.file);
    analyses.push(analysis);
  }

  const plans = [];
  const removedPlans = [];

  if (!hasSelectionFilters && !normalizedOptions.clean) {
    for (const existingEntry of existingMap.entries) {
      if (selectedSourceKeys.has(existingEntry.sourceKey)) {
        continue;
      }

      removedPlans.push({
        sourceKey: existingEntry.sourceKey,
        sourceFile: path.resolve(projectRoot, existingEntry.sourceFile),
        file: path.resolve(projectRoot, existingEntry.testFile),
        reason: 'Source file was removed from the full scan target.'
      });
    }
  }

  for (const skipped of skippedFiles) {
    if (skipped.stage !== 'analysis') {
      continue;
    }
    const sourceKey = safeRealpath(skipped.file);
    const existing = existingEntryMap.get(sourceKey);
    if (!existing) {
      continue;
    }

    removedPlans.push({
      sourceKey,
      sourceFile: path.resolve(projectRoot, existing.sourceFile),
      file: path.resolve(projectRoot, existing.testFile),
      reason: 'Source no longer exposes runtime exports.'
    });
  }

  for (const analysis of analyses) {
    const sourceKey = safeRealpath(analysis.file);
    const existing = existingEntryMap.get(sourceKey);
    const content = renderGeneratedTest({
      projectRoot,
      helperFile,
      outputFile: analysis.outputFile,
      analysis
    });
    const existingContent = fs.existsSync(analysis.outputFile)
      ? fs.readFileSync(analysis.outputFile, 'utf8')
      : null;

    let action = 'create';
    if (existingContent !== null) {
      action = existingContent === content ? 'unchanged' : 'update';
    }

    if (normalizedOptions.update && !fs.existsSync(analysis.outputFile)) {
      skippedFiles.push({
        file: analysis.file,
        reason: 'Update mode only refreshes existing generated files.',
        stage: 'mode'
      });
      continue;
    }

    if (existingContent !== null && !existingContent.startsWith(GENERATED_MARKER) && !normalizedOptions.force) {
      plans.push(buildConflictPlan(analysis, content, action, projectRoot));
      continue;
    }

    plans.push(buildPlan(analysis, content, action, existing, projectRoot));
  }

  if (normalizedOptions.clean) {
    const cleanTargets = resolveCleanTargets({
      analyses,
      existingMap,
      projectRoot,
      selectedSourceKeys,
      hasSelectionFilters,
      changedSet
    });

    for (const cleanTarget of cleanTargets) {
      removedPlans.push(cleanTarget);
    }

    plans.length = 0;
  }

  const mergedMapEntries = mergeGenerateMapEntries({
    existingEntries: existingMap.entries,
    plans,
    removedPlans,
    review: normalizedOptions.review,
    clean: normalizedOptions.clean,
    projectRoot
  });

  const createdFiles = [];
  const updatedFiles = [];
  const unchangedFiles = [];
  const conflictFiles = [];

  for (const plan of plans) {
    if (plan.action === 'conflict') {
      conflictFiles.push(plan.outputFile);
      continue;
    }

    if (plan.action === 'create') {
      createdFiles.push(plan.outputFile);
    } else if (plan.action === 'update') {
      updatedFiles.push(plan.outputFile);
    } else {
      unchangedFiles.push(plan.outputFile);
    }
  }

  let removedFiles = [];
  let helperRemoved = false;

  if (!normalizedOptions.review) {
    const expectedManagedFiles = new Set();

    if (mergedMapEntries.length > 0 && !normalizedOptions.clean) {
      fs.mkdirSync(outputDir, { recursive: true });
      writeManagedFile(helperFile, GENERATED_HELPER_SOURCE, { force: normalizedOptions.force });
      expectedManagedFiles.add(safeRealpath(helperFile));
    }

    for (const plan of plans) {
      if (plan.action === 'conflict') {
        continue;
      }

      if (plan.action === 'create' || plan.action === 'update') {
        writeManagedFile(plan.outputFile, plan.content, { force: normalizedOptions.force });
      }
    }

    for (const entry of mergedMapEntries) {
      expectedManagedFiles.add(safeRealpath(path.resolve(projectRoot, entry.testFile)));
    }

    const explicitRemoved = removePlannedFiles(removedPlans, outputDir);
    removedFiles = cleanupStaleGeneratedFiles(outputDir, expectedManagedFiles, explicitRemoved);

    if (mergedMapEntries.length > 0) {
      writeGenerateMap(mapFile, projectRoot, scanTarget, outputDir, mergedMapEntries);
    } else {
      removeGenerateMap(mapFile);
      helperRemoved = removeManagedHelper(helperFile, outputDir);
    }
  }

  const entries = buildSummaryEntries({
    plans,
    removedPlans,
    projectRoot,
    review: normalizedOptions.review
  });
  const summary = {
    targetDir: scanTarget.requestedPath,
    outputDir,
    helperFile,
    mapFile,
    scannedFiles: candidateFiles,
    generatedFiles: plans
      .filter((plan) => plan.action !== 'conflict')
      .map((plan) => plan.outputFile),
    removedFiles,
    skippedFiles,
    createdFiles,
    updatedFiles,
    unchangedFiles,
    cleanedFiles: normalizedOptions.clean ? removedFiles : [],
    conflictFiles,
    entries,
    review: normalizedOptions.review,
    update: normalizedOptions.update,
    clean: normalizedOptions.clean,
    changed: normalizedOptions.changed,
    plan: normalizedOptions.plan,
    writeHints: normalizedOptions.writeHints,
    filters: buildFilterSummary(normalizedOptions, projectRoot),
    gateOptions: {
      strict: normalizedOptions.strict,
      failOnSkips: normalizedOptions.failOnSkips,
      failOnConflicts: normalizedOptions.failOnConflicts,
      requireConfidence: normalizedOptions.requireConfidence
    },
    hintFiles: {
      created: createdHintFiles,
      updated: updatedHintFiles,
      unchanged: unchangedHintFiles
    },
    artifacts: {
      generateMap: mapFile,
      helperFile,
      generateResult: resultArtifactFile,
      generateHandoff: handoffArtifactFile,
      generateBacklog: backlogArtifactFile
    },
    gates: null,
    backlog: null,
    prompt: '',
    helperRemoved
  };

  summary.gates = buildGenerateGateSummary(summary);
  summary.backlog = buildGenerateBacklog(summary, projectRoot);
  summary.prompt = buildPromptSummary(summary, projectRoot);

  return summary;
}

